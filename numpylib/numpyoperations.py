import numpy as np

my_numpy_arr = np.arange(1,11)
print(my_numpy_arr)

add_arr = my_numpy_arr+my_numpy_arr
print(add_arr)

# Adding scalar element into the array
result_arr = my_numpy_arr * 10
print(result_arr)

# Universal operations on array
numpy_result_arr = np.sqrt(my_numpy_arr)
print(numpy_result_arr)